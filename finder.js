const adUrl = "https://assetdelivery.roblox.com/v1/asset/?id=";

const meshList = uris.split("\n").map(url => {
    url = url.toLowerCase();

    if (url.startsWith("rbxassetid://"))
        return url.substring(13);
    else if (url.startsWith("rbxasset://"))
        return null;
    else if (url.startsWith(urlWSlash))
        return url.substring(urlWSlash.length);
    else if (url.startsWith(urlWoSlash))
        return url.substring(urlWoSlash.length);
    else if (url.startsWith(adUrl))
        return url.substring(adUrl.length);
    else if (url.startsWith(urlWoWWW))
        return url.substring(urlWoWWW.length);

    console.log("HMM", url);
    return null;
});

(async function () {
    for (const id of meshList) {
        if (id === null)
            continue;

        if (ids[id]) {
            console.log("SKIP", id);
            continue;
        }

        await axios.get(`https://assetdelivery.roblox.com/v2/asset?id=${id}`, {
            headers: {
                "Accept": "application/json"
            }
        })
            .then(res => {
                if (!res.data.errors) {
                    console.log("OK", id);
                    ids[id] = true;
                    const hash = res.data.locations[0].location.split("/").slice(-1)[0];
                    manifest[id] = hash;
                }
            })
            .catch(err => {
                if (err.response && err.response.data.errors) {
                    const message = err.response.data.errors[0].message;

                    if (message === "Requested asset is archived") {
                        console.log("ARCHIVED", id);
                        ids[id] = true;
                        manifest[id] = "archived";
                    } else if (message === "Asset is not approved for the requester") {
                        console.log("NOT APPROVED", id);
                        ids[id] = true;
                        manifest[id] = "not approved";
                    }
                } else {
                    console.log("ERR", id, err);
                }
            });

        fs.writeFileSync("manifest.json", JSON.stringify({ manifest, ids }));
    }
})();
